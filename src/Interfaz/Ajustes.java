    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        JFileChooser selector = new JFileChooser();
        
        selector.setFileFilter(new FileNameExtensionFilter(
                "Imagenes", 
                "jpg", 
                "png",
                "gif",
                "jpeg"
        ));
        
        if(selector.showDialog(null, "Aceptar") != JFileChooser.APPROVE_OPTION)
            return;        
        
        File logo = selector.getSelectedFile();
        
        txtLogo.setText(logo.getAbsolutePath());
        lblLogo.setIcon(new ImageIcon(new ImageIcon(txtLogo.getText())
                .getImage().getScaledInstance(400, 200, Image.SCALE_SMOOTH)));
        
    }//GEN-LAST:event_jButton4ActionPerformed

