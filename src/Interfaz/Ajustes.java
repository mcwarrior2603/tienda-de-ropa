    private void guardarConfiguración(){
        File config = new File("Archivos/config.txt");                
        
        PrintWriter writer;
        try {
            writer = new PrintWriter(config);
            writer.println(lblColorFondo.getBackground().getRed());
            writer.println(lblColorFondo.getBackground().getGreen());
            writer.println(lblColorFondo.getBackground().getBlue());
            writer.println(lblColorPaneles.getBackground().getRed());
            writer.println(lblColorPaneles.getBackground().getGreen());
            writer.println(lblColorPaneles.getBackground().getBlue());
            writer.println(lblColorMenu.getBackground().getRed());
            writer.println(lblColorMenu.getBackground().getGreen());
            writer.println(lblColorMenu.getBackground().getBlue());
            writer.println(txtLogo.getText());
            writer.println(chkAnticipo.isSelected());
            writer.println(txtPorcentaje.getText());
            writer.println(txtHost.getText());
            writer.println(txtPuerto.getText());
            writer.println(txtDBName.getText());
            writer.close();
            Configuracion.cargarConfiguracion();
            JOptionPane.showMessageDialog(
                    this,
                    "Configuración guardada correctamente,\n"
                            + "reinicie el sistema para que lo cambios\n"
                            + "surtan efecto"
            );
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Configuracion.class.getName()).log(Level.SEVERE, null, ex);
        }     
    }
    
