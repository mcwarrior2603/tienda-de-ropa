    public static void mostrar(JFrame parent, int idUsuario){
        ConsultarTransaccion dialogo = new ConsultarTransaccion(parent, true, idUsuario);
        
        dialogo.setVisible(true);
        dialogo.dispose();
    }
     
    private void consultar(){
        String tipo = cboTipo.getSelectedItem().toString();        
        int folio = Integer.parseInt(txtFolio.getText());
        try {
            if (tipo.equalsIgnoreCase("INGRESO")) {

                String sql = "SELECT FOLIO FROM INGRESOS WHERE FOLIO=" + folio;

                ResultSet consulta = SQLConnection.select(sql);

                if (consulta.next()){
                    setVisible(false);
                    dispose();
                    Ingreso.mostrar(null, idUsuario, folio);
                }else 
                    JOptionPane.showMessageDialog(this, "Folio no encontrado");                
            } else if (tipo.equalsIgnoreCase("EGRESO")) {

                String sql = "SELECT FOLIO FROM EGRESOS WHERE FOLIO=" + folio;

                ResultSet consulta = SQLConnection.select(sql);

                if (consulta.next()){
                    setVisible(false);
                    dispose();
                    Egreso.mostrar(null, idUsuario, folio);
                }else 
                    JOptionPane.showMessageDialog(this, "Folio no encontrado");                
            } else if (tipo.equalsIgnoreCase("COMPRA")) {

                String sql = "SELECT FOLIO FROM COMPRAS WHERE FOLIO=" + folio;               
                
                ResultSet consulta = SQLConnection.select(sql);

                if (consulta.next()){
                    setVisible(false);
                    dispose();
                    EntradaMercancia.mostrar(null, idUsuario, folio);
                }else 
                    JOptionPane.showMessageDialog(this, "Folio no encontrado");                
            }else if (tipo.equalsIgnoreCase("VENTA")) {

                String sql = "SELECT FOLIO FROM REPORTE_VENTA WHERE FOLIO=" + folio;               
                
                ResultSet consulta = SQLConnection.select(sql);

                if (consulta.next()){
                    setVisible(false);
                    dispose();
                    Venta.mostrar(null, idUsuario, folio);
                }else 
                    JOptionPane.showMessageDialog(this, "Folio no encontrado");                
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConsultarTransaccion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
